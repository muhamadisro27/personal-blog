import type { Metadata } from "next"
import "./globals.css"
import { ThemeProvider } from "@/components/theme-provider"
import { SidebarProvider } from "@/components/ui/sidebar"
import { Sidebar } from "@/components/molecules/AppSidebar"
import Header from "@/components/organism/Header"
import { Container } from "@/components/atoms/Container"
import { Geist } from "next/font/google"
import AppInitializer from "@/components/molecules/AppInitializer"
import LoadingPage from "@/components/pages/LoadingPage"
import { Analytics } from "@vercel/analytics/next"
import { SpeedInsights } from "@vercel/speed-insights/next"
import { APP_MODE } from "@/utils/constant"
import { runtimeConfig } from "@/lib/config"
import PageProgress from "@/components/molecules/PageProgress"
import { Toaster } from "@/components/ui/toaster"

export const metadata: Metadata = {
  title: "Muhamad Isro Sabanur | Personal Website",
  description: "Generated by create next app",
}

const geist = Geist({
  subsets: ["latin"],
  fallback: ["monospace"],
})

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode
}>) {
  const { appMode } = runtimeConfig()

  const renderAnalytic = () =>
    appMode === APP_MODE.production ? <Analytics /> : <></>
  const renderSpeedInsight = () =>
    appMode === APP_MODE.production ? <SpeedInsights /> : <></>

  const renderChildren = () =>
    appMode === APP_MODE.production ? (
      <LoadingPage>{children}</LoadingPage>
    ) : (
      <>{children}</>
    )

  return (
    <html lang="en" suppressHydrationWarning>
      <body className={geist.className}>
        {renderAnalytic()}
        {renderSpeedInsight()}
        <ThemeProvider
          attribute="class"
          defaultTheme="dark"
          enableSystem
          disableTransitionOnChange
        >
          <SidebarProvider>
            <AppInitializer>
              <PageProgress />
              <Sidebar />
              <main className="w-full min-h-svh">
                <div className="relative min-h-screen bg-gray-450 overflow-hidden">
                  <div className="absolute w-72 h-72 bg-purple-600/10 rounded-full top-20 left-10 blur-3xl animate-pulse"></div>

                  <div className="absolute w-96 h-96 bg-pink-600/10 rounded-full bottom-20 right-20 blur-3xl animate-pulse"></div>

                  <div className="relative z-10">
                    <Header />
                    <Container>{renderChildren()}</Container>
                  </div>
                </div>
                {/* <AmbientBackground> */}
                {/* </AmbientBackground> */}
                <Toaster />
              </main>
            </AppInitializer>
          </SidebarProvider>
        </ThemeProvider>
      </body>
    </html>
  )
}
